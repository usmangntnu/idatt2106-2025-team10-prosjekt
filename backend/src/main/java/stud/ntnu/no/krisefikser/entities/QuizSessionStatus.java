package stud.ntnu.no.krisefikser.entities;

/**
 * Enum representing the status of a quiz attempt.
 * <p>
 * The status of a quiz attempt can be one of the following:
 * <ul>
 * <li>IN_PROGRESS: The quiz attempt is currently ongoing.</li>
 * <li>COMPLETED: The quiz attempt has been finished.</li>
 * </ul>
 * </p>
 */
public enum QuizSessionStatus {
  IN_PROGRESS,
  COMPLETED
}
