package stud.ntnu.no.krisefikser.entities.map;

/**
 * Enumeration representing the severity levels of events.
 */
public enum EventSeverity {
  /**
   * Represents a low severity level.
   */
  LOW,
  /**
   * Represents a medium severity level.
   */
  MEDIUM,
  /**
   * Represents a high severity level.
   */
  HIGH,
  /**
   * Represents a critical severity level.
   */
  CRITICAL
}
